#explicit typecasting is done manually through a programmer or developer
#typecasting is converting a data type into another datatype
a="1"
b="2"
print(a+b)
#implicit casting is done by python intrepreter automatically for example:
c=8.8# it is a float data type
d=3# it is a int data type 
print(c+d) # it automatically take both as float python converts smaller data type into a higher data type to prevent data loss.
